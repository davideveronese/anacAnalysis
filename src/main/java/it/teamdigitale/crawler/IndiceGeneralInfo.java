package it.teamdigitale.crawler;

public class IndiceGeneralInfo {

    String identificativoPEC;
    String codiceFiscale;
    String ragioneSociale;
    String url;
    String dataUltimoTentativoAccessoUrl;
    String esitoUltimoTentativoAccessoUrl;

    public String getIdentificativoPEC() {
        return identificativoPEC;
    }

    public void setIdentificativoPEC(String identificativoPEC) {
        this.identificativoPEC = identificativoPEC;
    }

    public String getCodiceFiscale() {
        return codiceFiscale;
    }

    public void setCodiceFiscale(String codiceFiscale) {
        this.codiceFiscale = codiceFiscale;
    }

    public String getRagioneSociale() {
        return ragioneSociale;
    }

    public void setRagioneSociale(String ragioneSociale) {
        this.ragioneSociale = ragioneSociale;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getDataUltimoTentativoAccessoUrl() {
        return dataUltimoTentativoAccessoUrl;
    }

    public void setDataUltimoTentativoAccessoUrl(String dataUltimoTentativoAccessoUrl) {
        this.dataUltimoTentativoAccessoUrl = dataUltimoTentativoAccessoUrl;
    }

    public String getEsitoUltimoTentativoAccessoUrl() {
        return esitoUltimoTentativoAccessoUrl;
    }

    public void setEsitoUltimoTentativoAccessoUrl(String esitoUltimoTentativoAccessoUrl) {
        this.esitoUltimoTentativoAccessoUrl = esitoUltimoTentativoAccessoUrl;
    }
}
